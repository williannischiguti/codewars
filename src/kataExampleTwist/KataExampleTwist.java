package kataExampleTwist;

public class KataExampleTwist {

	public static String[] kataExampleTwist() {
		
		String[] websites = new String [1000];
		
		for (int i = 0; i < websites.length; i ++) {
			websites[i] = "codewars";
		}
	
		return websites;
	}

	public static void main(String args[]) {
		
		kataExampleTwist();
		
	}

}
